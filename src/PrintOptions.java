/**
 * Enum to act as a switch for the print function to avoid 
 * rewriting of several functions
 * @author Joey Rodgers jdr14
 * @version 1.0.0
 */
public enum PrintOptions 
{
    /**
     * DEFAULT is the first option  for print
     */
    DEFAULT,
    
    /**
     * LENGTHS is the second options for print
     */
    LENGTHS,
    
    /**
     * STATS is the third option for print
     */
    STATS;
}
